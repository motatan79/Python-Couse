# Hexadecimal representation
print(hex(12))

# Binary representation
print(f'this is binary {bin(1234)}')

print(f'this is binary {bin(512)}')

# Para elevar un numero, el tercer argumento es el resultante de dividir el numero elevado entre dicho número
print(pow(2,4,2))

# Para valores absolutos
print(abs(2))

# Para redondear numeros, siempre retorna un floating numbers
print(round(3.1))
# si queremos especificar la cantidad de decimales está el segundo argumento
print(round(3.141592, 2))

